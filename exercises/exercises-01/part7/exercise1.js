function createConsecutiveMatrix() {
  let matrix = []; // המטריצה הראשית (הטבלה)
  let counter = 0; // המספר שאנחנו מכניסים, מתחיל מ-0

  // לולאה חיצונית: בונה 10 שורות
  for (let i = 0; i < 10; i++) {
    let row = []; // בכל סיבוב ניצור שורה ריקה חדשה

    // לולאה פנימית: ממלאת 10 תאים בתוך השורה הנוכחית
    for (let j = 0; j < 10; j++) {
      row.push(counter); // דוחפים את המספר הנוכחי לשורה
      counter++; // מקדמים את המספר לפעם הבאה (1, 2, 3...)
    }

    // אחרי שהשורה התמלאה ב-10 מספרים, נדחוף אותה למטריצה הראשית
    matrix.push(row);
  }

  // הדפסת המטריצה כטבלה מסודרת
  console.table(matrix);
}

// הפעלת הפונקציה
createConsecutiveMatrix();
